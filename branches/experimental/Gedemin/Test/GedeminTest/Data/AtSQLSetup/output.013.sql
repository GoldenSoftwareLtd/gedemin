SELECT 
  F1.ID, 
  F1.VERSIONSTRING, 
  F1.RELEASEDATE, 
  F1.COMMENT, 
  F2.ID, 
  F2.VERSIONSTRING, 
  F2.RELEASEDATE, 
  F2.COMMENT 
FROM 
  FIN_VERSIONINFO F1
    JOIN 
      FIN_VERSIONINFO F2
    ON 
      TRIM(F1.COMMENT)  =  TRIM(F2.COMMENT)
WHERE 
  TRIM(F1.COMMENT)  =  TRIM(:P)