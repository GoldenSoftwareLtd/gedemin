  ACTIVE
  ON CONNECT
  POSITION 0
AS
  DECLARE VARIABLE ingroup INTEGER = 0;
BEGIN
  SELECT ingroup FROM gd_user WHERE ibname = CURRENT_USER
    INTO :ingroup;
  RDB$SET_CONTEXT('USER_SESSION', 'GD_INGROUP', :ingroup);
END
